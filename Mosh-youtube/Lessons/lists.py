names = ['John', 'Bob', 'Mosh', 'Sarah', 'Mary']
print(names)
# prints the first item
print(names[0])
#  prints the last item
print(names[-1])
# second to last item
print(names[-2])
# prints third item and on
print(names[2:])
# prints items 3-4
print(names[2:4])
# prints all items
print(names[:])
# replaces first item in the list
names[0] = 'Jon'
print(names)