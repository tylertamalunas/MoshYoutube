# prints each letter on its own line
for item in 'Python':
    print(item)
# prints each word on its own line
for item in ['Mosh', 'Tyler', 'Sarah']:
    print(item)
# prints each number from 0-10 on its own line
for item in range(10):
    print(item)
# prints starting at 5 and ending before 10
for item in range(5, 10):
    print(item)
# prints starting at 5, and ending before 10, but skipping 2 spaces each time
for item in range(5, 10, 2):
    print(item)