print(10 + 3) # addition 13
print(10 - 3) # subtraction 7
print(10 * 3) # multiplication 30
print(10 / 3) # division 3.333333
print(10 // 3) # division non float
print(10 % 3) # division remainder 1
print(10 ** 3) # exponent 1000

x = 10
# increment
x = x + 3
print(x)
# augmented operator, shortens line 11
x = 10
x += 3
print(x)