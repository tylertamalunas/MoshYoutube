course = 'Python for Beginners'
# len() prints the length of the item
print(len(course))
# prints string in uppercase
print(course.upper())
# prints in lowercase
print(course.lower())
# finds which index the string is at
print(course.find('P'))
print(course.find('o'))
# 11 is where the string starts
print(course.find('Beginners'))
print(course)
# replaces string with specified string
print(course.replace('Beginners', 'Absolute Beginners'))
# checks the existence of a character or string
print('Python' in course)